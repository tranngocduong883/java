    private void dangNhap() {
        String user = jTextField1.getText().trim();
        String pass = new String(jPasswordField1.getPassword()).trim();

        if (user.isEmpty() || pass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ tên tài khoản và mật khẩu!");
            return;
        }

        UserDao dao = new UserDao();
        boolean ok = dao.kiemTraDangNhap(user, pass);

        if (ok) {
            JOptionPane.showMessageDialog(this, "Đăng nhập thành công!");

            // MỞ MÀN HÌNH CHÍNH
            jfmanhinhchinh main = new jfmanhinhchinh();
            main.setVisible(true);

            // ĐÓNG MÀN HÌNH ĐĂNG NHẬP
            this.dispose();

        } else {
            JOptionPane.showMessageDialog(this, "Sai tài khoản hoặc mật khẩu!");
        }
    }

